void dijkstra (int source,int destination,int graph[56][56], vector<string>&stations)
{

    vector<int>distance(56, INT_MAX);
    vector<int>predecessor(56, -1);

    // Min-heap priority queue to store (distance, vertex)
    priority_queue<int, vector<int>, greater<int>> pq;

    distance[source] = 0;
    pq.push(source);

    while (!pq.empty())
    {
        int u = pq.top();
        pq.pop();


        for (int v = 0; v < 56; ++v)
        {
            if (graph[u][v] && distance[u] + graph[u][v] < distance[v])
            {
                distance[v] = distance[u] + graph[u][v];
                pq.push(v);
                predecessor[v] = u;
            }
        }
    }
    
    //Printing Path between stations.
    if (distance[destination] == INT_MAX){
        cout << "There is no path from " << stations[source] << " to " << stations[destination] <<". "<<endl;
    }
    else{
        cout << "Minimum stations from " << stations[source] << " to " << stations[destination] << " are " << distance[destination] << "."<<"\n\n";
       //Calling the Printpath function
        printPath(source, destination, predecessor, stations);
    }
   
}




