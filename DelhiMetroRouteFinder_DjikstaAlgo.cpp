//******************Printing Function*******************
void printPath(int source, int destination, vector<int>&predecessor, vector<string>&stations)
{
    stack<int> path;
    int intermediate_station = destination;
    path.push(intermediate_station);
    while (predecessor[intermediate_station] != -1)
    {
        path.push(predecessor[intermediate_station]);
        intermediate_station = predecessor[intermediate_station];
    }

    //Printing the Path
    cout << "Path From " << stations[source] << " To " << stations[destination] << " : "<<endl;
    while (!path.empty())
    {
        cout << stations[path.top()];
        if (path.size() != 1)cout << " -> ";
        path.pop();
    }
    cout <<"\n\n";
}


